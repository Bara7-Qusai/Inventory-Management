    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableExpenes.getModel();
        try {

            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from expenes ");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("expense_pk"), rs.getDate("expenesDate"), rs.getString("expenesName"), rs.getInt("exoenseprice")});
            }
            // setVisible(false);
            // new ManageExpenese().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_formComponentShown

