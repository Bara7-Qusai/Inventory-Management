    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String priceExpenes = price.getText();

        // SimpleDateFormat myFormat = new SimpleDateFormat("dd-MM-yyyy");
        Calendar cal = Calendar.getInstance();
        long milills = System.currentTimeMillis();
        Date today = new Date(milills);

        try {

            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("insert into expenes ( expenesDate,expenesName,exoenseprice) values(?,?,?)");
            ps.setDate(1, today);
            ps.setString(2 ,name);
            ps.setString(3, priceExpenes);
            

           // JOptionPane.showMessageDialog(null, " " + name);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, " Expenese Add Successflly");
            setVisible(false);
            new ManageExpenese().setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_btnSaveActionPerformed

